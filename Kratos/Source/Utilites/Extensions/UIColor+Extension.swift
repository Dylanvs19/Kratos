import UIKit

extension UIColor {
    
    static var kratosBlue: UIColor {
        return UIColor(red: 0, green: 0, blue: 100/255, alpha: 1)
    }
    static var darkBlue: UIColor {
        return UIColor(red: 25/255, green: 25/255, blue: 112/255, alpha: 1)
    }
    static var kratosRed: UIColor {
        return UIColor(red: 207/255, green: 0, blue:0, alpha: 1)
    }
    static var kratosGreen: UIColor {
        return UIColor(red: 126/255, green: 211/255, blue: 33/255, alpha: 1)
    }
    static var slate: UIColor {
        return UIColor(red: 243/255, green: 243/255, blue: 243/255, alpha: 1)
    }
}
